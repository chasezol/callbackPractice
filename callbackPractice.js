var contains = function(array, str, cb){
  for(i = 0; i < array.length; i++){
    if(array[i] === str){
       return cb(true);
    }else{
      return cb(false);
    }
  }
}

var names = ['Tyler', 'Cahlan', 'Ryan', 'Colt', 'Tyler', 'Blaine', 'Cahlan'];
